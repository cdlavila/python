"""
Comparison operators
  == strict equality
  != not equal strict equality
  > greater than
  < less than
  >= greater than or equal to
  <= less than or equal to
"""
print('\nComparison operators')
x = 2
y = 1 + 1
print(x == y)  # true
print(x != y)  # false
print(x > y)  # false
print(x < y)  # false
print(x >= y)  # true
print(x <= y)  # true